package com.info.java_basics;

public class Datatypes {
	
	public static void main(String[] args) {
        int a = 10;
        float b = 5.5f;
        char c = 'A';
        boolean d = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
